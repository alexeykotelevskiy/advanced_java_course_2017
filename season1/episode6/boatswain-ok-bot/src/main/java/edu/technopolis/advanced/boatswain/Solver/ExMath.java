package edu.technopolis.advanced.boatswain.Solver;

public class ExMath {

    public static double ctg(double x)
    {
        return 1/Math.tan(x);
    }

    public static double sec(double x)
    {
        return 1 / Math.cos(x);
    }

    public static double cosec(double x)
    {
        return 1 / Math.sin(x);
    }
}
